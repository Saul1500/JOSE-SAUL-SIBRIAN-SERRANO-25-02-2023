using System;

namespace For
{
    class For
    {
        static void Main(string[] args)
        {
            
            int resultado;

            Console.WriteLine("Ingrese el Primer Numero: ");
            Console.WriteLine("Ingrese el Segundo Numero: ");
            
            for (int i = 1; i <= 0; i++)
            {
                resultado = i % 2;
                Console.WriteLine(resultado.ToString());
            }
        }
    }
}
